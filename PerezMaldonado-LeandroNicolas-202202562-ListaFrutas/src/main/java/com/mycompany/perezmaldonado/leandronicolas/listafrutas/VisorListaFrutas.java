    private void aniadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aniadirActionPerformed
        if(nombre.getText().isEmpty()==false || precio.getText().isEmpty()==false){
            Integer pre = Integer.parseInt(precio.getText());
            Fruta nueva = new Fruta(nombre.getText(),pre);
            frutas.aniadirFruta(nueva);
        }else{
            JOptionPane.showMessageDialog(null,"Porfavor ingrese los valores correspondientes");
        }
    }//GEN-LAST:event_aniadirActionPerformed

    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        if(nombreEli.getText().isEmpty()==false){
            frutas.eliminarFruta(nombreEli.getText());
        }
    }//GEN-LAST:event_eliminarActionPerformed

